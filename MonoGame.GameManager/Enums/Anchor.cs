namespace MonoGame.GameManager.Enums
{
    /// <summary>
    /// An Anchor is a pre-defined position in parent control that we use to position a child.
    /// For example, we can use anchors to position a control at the bottom-center point of its parent.
    /// </summary>
    public enum Anchor
    {
        Center,
        TopLeft,
        TopRight,
        TopCenter,
        BottomLeft,
        BottomRight,
        BottomCenter,
        CenterLeft,
        CenterRight
    };
}
